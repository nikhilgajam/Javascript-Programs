// Operators

var a=100, b=100;

// 1. Assignment Operator
a = b;  // a will contain a value 100

// 2. Arithmetic Operators
a + b;
a - b;
a * b;
a / b;
a % b;

// 3. Increment And Other Operator
a++;
a--;
a += 1; // we can place all arithmetic before = (Ex: +=, -=, *=, /=)

// 4. Boolean Operators (Comparision Operators)

a == b;
a != b;
a === b; // Both value and type should be same
a !== b;
a > b;
a < b;
a >= b;
a <= b;

// 5. Logical Operators

a && b; // Both variable should contain true values to return true
a || b; // One true valued variable is enough to return true
!a;     // This turns true values to false and false values to true ( !(1 == 2) returns true)

// 6.Bitwise Operators

a & b; // The value of a will in binary and if both are true then the return value will be true according to number of binary digits
a | b; // The value of a will in binary and if one of them are true then the return value will be true according to number of binary digits
a ^ b; // This returns true when one is true and other is false only




// These are all operators in Javascript
