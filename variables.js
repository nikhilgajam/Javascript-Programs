// Variables

var name = "Hello"; // when we use var then a global variable is declared even inside some block

let say = "OK"; // when we use let then a local variable is created and when it is used in starting it can be global all the scope rules will be there

const c = "Constant"; // when we declare a constant we cannot change it

// Variables can have letters, digits, underscores and dollar sign($) and cannot start with digits and are case sensitive

document.write(name, say, c);
